class Book:
    def __init__(self, id, title, author, published_year):
        self.id = id
        self.title = title
        self.author = author
        self.published_year = published_year

class Member:
    def __init__(self, id, name, membership_date):
        self.id = id
        self.name = name
        self.membership_date = membership_date
